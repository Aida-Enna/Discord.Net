namespace Discord.Net.Models
{
    /// <summary>
    ///     Represents the visibility type.
    /// </summary>
    public enum VisibilityType
    {
        /// <summary>
        ///     Invisible to everyone except the user themselves.
        /// </summary>
        None = 0,

        /// <summary>
        ///     Visible to everyone.
        /// </summary>
        Everyone = 1,
    }
}
