namespace Discord.Net.Models
{
    /// <summary>
    ///     Represents the button style type.
    /// </summary>
    public enum ButtonStyle
    {
        /// <summary>
        ///     Blurple.
        /// </summary>
        Primary = 1,

        /// <summary>
        ///     Grey.
        /// </summary>
        Secondary = 2,

        /// <summary>
        ///     Green.
        /// </summary>
        Success = 3,

        /// <summary>
        ///     Red.
        /// </summary>
        Danger = 4,

        /// <summary>
        ///     Grey, navigates to a URL.
        /// </summary>
        Link = 5,
    }
}
